QString getPinyin1Astr(const QString& hzstr)
{
    QString pystr;

    for(int i=0; i<hzstr.count(); i++) {
        QChar ch = hzstr.at(i);
        ushort co = ch.unicode();
        if( isUnicodeHz(co)) {
            QChar py = Pinyin_Table[co- MMI_UNICODE_ISN_FIRST];
            pystr.append( py);
        }
        else {
            pystr.append( ch);
        }
    }
    return pystr;
}

